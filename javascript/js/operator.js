//Arithmetcic

let x = 27;
let y = 5;

//ADD (+)
console.log(">>>> Sum");
let sum = x + y;
console.log(sum);

//Sub
console.log(">>>> Diff");
let diff = x - y;
console.log(diff);

//Mul
console.log(">>>> Mul");
let mul = x * y;
console.log(mul);

//Div
console.log(">>>> Div");
let quotient = x / y;
let remainder = x % y;

console.log(quotient);
console.log(remainder);

// String Operations

let firstName = "Gershon";
let lastName = "Kumar";

let fullName = firstName + " " + lastName;

console.log(fullName);

// increment / decrement operator

x++; //x = x + 1;
x--; //x = x -1;
console.log(x);
